const std = @import("std");
const Allocator = std.mem.Allocator;
const Managed = std.math.big.int.Managed;
const rsa = @import("rsa.zig");

pub const DhParams = struct {
    dh_prime: [256]u8, // 2048-bit prime, big-endian
    g: u32, // generator (usually 2, 3, or 5)
};

pub const DhResult = struct {
    g_b: [256]u8, // g^b mod p, big-endian, send to server
    secret: [256]u8, // g_a^b mod p, big-endian, the auth key
};

pub fn compute(
    params: DhParams,
    g_a: *const [256]u8, // server's g^a mod p
    b_bytes: *const [256]u8, // caller-provided random exponent
    allocator: Allocator,
) !DhResult {
    var b = try Managed.init(allocator);
    defer b.deinit();
    var g_val = try Managed.initSet(allocator, params.g);
    defer g_val.deinit();
    var p = try Managed.init(allocator);
    defer p.deinit();
    var ga = try Managed.init(allocator);
    defer ga.deinit();
    var gb = try Managed.init(allocator);
    defer gb.deinit();
    var secret = try Managed.init(allocator);
    defer secret.deinit();

    try rsa.setFromBigEndianBytes(&b, b_bytes);
    try rsa.setFromBigEndianBytes(&p, &params.dh_prime);
    try rsa.setFromBigEndianBytes(&ga, g_a);

    // Validate DH parameters per MTProto spec and security guidelines.
    var one = try Managed.initSet(allocator, 1);
    defer one.deinit();
    var p_minus_one = try Managed.init(allocator);
    defer p_minus_one.deinit();
    try p_minus_one.sub(&p, &one);
    // 2^{2048-64} = 2^1984: in 256-byte big-endian, byte 7 = 1, rest 0.
    var safety_bytes: [256]u8 = @splat(0);
    safety_bytes[7] = 1;
    var safety = try Managed.init(allocator);
    defer safety.deinit();
    try rsa.setFromBigEndianBytes(&safety, &safety_bytes);
    var p_minus_safety = try Managed.init(allocator);
    defer p_minus_safety.deinit();
    try p_minus_safety.sub(&p, &safety);

    // 1 < g_a < p-1
    if (ga.toConst().order(one.toConst()) != .gt) return error.DhParamOutOfRange;
    if (ga.toConst().order(p_minus_one.toConst()) != .lt) return error.DhParamOutOfRange;
    // 2^1984 < g_a < p - 2^1984 (recommended safety margin)
    if (ga.toConst().order(safety.toConst()) != .gt) return error.DhParamOutOfRange;
    if (ga.toConst().order(p_minus_safety.toConst()) != .lt) return error.DhParamOutOfRange;

    // g_b = g^b mod p
    try rsa.modPow(&gb, &g_val, &b, &p, allocator);
    // secret = g_a^b mod p
    try rsa.modPow(&secret, &ga, &b, &p, allocator);

    // 1 < g_b < p-1
    if (gb.toConst().order(one.toConst()) != .gt) return error.DhParamOutOfRange;
    if (gb.toConst().order(p_minus_one.toConst()) != .lt) return error.DhParamOutOfRange;
    // 2^1984 < g_b < p - 2^1984
    if (gb.toConst().order(safety.toConst()) != .gt) return error.DhParamOutOfRange;
    if (gb.toConst().order(p_minus_safety.toConst()) != .lt) return error.DhParamOutOfRange;

    // SAFETY: both fields fully written by bigEndianBytes before return
    var result: DhResult = undefined;
    try rsa.bigEndianBytes(&gb, &result.g_b, allocator);
    try rsa.bigEndianBytes(&secret, &result.secret, allocator);
    return result;
}

